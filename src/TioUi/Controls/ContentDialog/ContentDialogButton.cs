namespace TioUi.Controls;

/// <summary>
/// 指定 ContentDialog 的默认按钮
/// </summary>
public enum ContentDialogButton
{
    /// <summary>
    /// 没有指定默认按钮
    /// </summary>
    None = 0,

    /// <summary>
    /// 主要按钮为默认按钮
    /// </summary>
    Primary = 1,

    /// <summary>
    /// 次要按钮为默认按钮
    /// </summary>
    Secondary = 2,

    /// <summary>
    /// 关闭按钮为默认按钮
    /// </summary>
    Close = 3
}